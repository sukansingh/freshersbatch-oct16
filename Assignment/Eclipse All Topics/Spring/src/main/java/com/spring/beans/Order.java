package com.spring.beans;

public class Order 
{
	private String name;
	
	private Item item;

	public Order() {}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Item getItem() {
		return item;
	}

	public void setItem(Item item) {
		this.item = item;
	}
	
	
	
	
}
